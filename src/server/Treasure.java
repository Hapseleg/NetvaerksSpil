package server;

public class Treasure {
    private int x, y, point;
    
    public Treasure(int x, int y, int point) {
        super();
        this.x = x;
        this.y = y;
        this.point = point;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
    public int getPoint() {
        return point;
    }
    
}
